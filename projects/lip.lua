Lip is a Lisp like language in Lua. It is designed to support functional programming in Lua.

With Lip we can write lisp-alike code:
{Lisp.lambda, 'compose', {'f', 'g'},
 {Lisp.lambda, {'x'}, {'f', {'g', {'x'}}}}
